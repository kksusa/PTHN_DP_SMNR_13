class AgeException(Exception):
    def __init__(self, age):
        self.age = age

    def __str__(self):
        return f'–í–æ–∑—Ä–∞—Å—Ç {self.age} –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–π –≤–µ–ª–∏—á–∏–Ω–æ–π.'


class Human:
    def __init__(self, age, surname, name, patronymic):
        if age >= 0:
            self.__age = age
        else:
            raise AgeException(age)
        self.surname = surname
        self.name = name
        self.patronymic = patronymic

    def birthday(self):
        self.__age += 1

    def get_age(self):
        return self.__age

    def full_name(self):
        return f"{self.surname} {self.name} {self.patronymic}"


